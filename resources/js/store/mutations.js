export default {
  /**
   * * Set the media value.
   * @param {object} state
   * @param {string} value
   */
  setMedia (state, value) {
    state.media = value;
  },
  /**
   * * Set the width value.
   * @param {object} state
   * @param {string} value
   */
  setWidth (state, value) {
    state.width = value;
  },
};